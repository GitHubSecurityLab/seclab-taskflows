@mcp.tool()
def tmin(
    afl_binary_path: Annotated[str, Field(description="Path to the AFL-instrumented binary")],
    crash_input: Annotated[str, Field(description="Path to the crash input")],
    output_path: Annotated[str, Field(description="Where to write the minimised input")],
    stdin_input: Annotated[bool, Field(description="If true, minimise via stdin (rare; libAFLDriver-based binaries must use file mode)")] = False,
    extra_args: Annotated[list[str] | None, Field(description="Extra CLI args for the binary; '@@' will be replaced with the input path")] = None,
    timeout_seconds: Annotated[int, Field(description="afl-tmin timeout")] = 60,
) -> dict:
    """Run afl-tmin on a crash input.

    By default we use file mode (afl-tmin appends ``@@`` for the input path).
    The libAFLDriver-based AFL binaries built by ``compile_harness`` loop forever
    on stdin in standalone mode, so file mode is the only one that works for
    them.
    """
    extra_args = list(extra_args or [])
    tool = _which("afl-tmin")
    if not tool:
        return {"ok": False, "error": "afl-tmin not on PATH"}
    cmd = [tool, "-i", crash_input, "-o", output_path]
    target_args = extra_args if (stdin_input or "@@" in extra_args) else [*extra_args, "@@"]
