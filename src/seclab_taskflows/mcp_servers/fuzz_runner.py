# Static call graph + reachability (E3 — Fuzz-Introspector lite)
# ---------------------------------------------------------------------------

@mcp.tool()
def build_call_graph(
    source_root: Annotated[str, Field(description="Path to the unpacked source tree (e.g. the repo root)")],
    cov_lcov_path: Annotated[str, Field(description="Optional LCOV tracefile to mark which functions were reached")] = "",
    output_dot: Annotated[str, Field(description="Where to write the Graphviz .dot file")] = "",
    output_svg: Annotated[str, Field(description="Where to write the rendered SVG (requires graphviz dot on PATH)")] = "",
    max_nodes: Annotated[int, Field(description="Hard cap on functions to include (largest reachable subgraph wins)")] = 200,
) -> dict:
    """Build a static call graph using ``ctags`` + ``cscope`` and annotate with
    coverage. This is a lightweight Fuzz-Introspector-style tool that lets the
    agent (and the dashboard) see which library functions the harness chain
    actually reaches versus which remain untouched.

    Output: a Graphviz ``.dot`` file (and SVG, if ``dot`` is installed) where:
      - reached functions are coloured green
      - unreached functions are coloured red
      - external library calls (functions defined elsewhere) are grey
    """
    root = Path(source_root)
    if not root.is_dir():
        return {"ok": False, "error": f"source_root not found: {source_root}"}

    ctags = _which("ctags")
    cscope = _which("cscope")
    if not ctags or not cscope:
        return {"ok": False, "error": "ctags and cscope are required (install_afl.sh installs them)"}

    # 1. Build a tags database for function definitions.
    tags_file = root / "_callgraph_tags"
    _run([ctags, "-R", "--c-kinds=fp", "--fields=+ne", "--languages=C,C++",
          "-f", str(tags_file), str(root)], timeout=DEFAULT_TIMEOUT)
    if not tags_file.is_file():
        return {"ok": False, "error": "ctags failed to produce a tags file"}

    # Parse function definitions (kind 'f') from the ctags file.
    func_defs: dict[str, str] = {}
    for line in tags_file.read_text(errors="replace").splitlines():
        if line.startswith("!"):
            continue
        parts = line.split("\t")
        if len(parts) < 4:
            continue
        name, file_, _addr = parts[0], parts[1], parts[2]
        kind = parts[3].split(";")[0]
        if kind == "f":
            func_defs.setdefault(name, file_)

    # 2. Build cscope database for call relationships.
    cscope_files = root / "_callgraph_files"
    file_list = []
    for ext in ("c", "cc", "cpp", "h", "hpp"):
        file_list.extend(str(p) for p in root.rglob(f"*.{ext}"))
    cscope_files.write_text("\n".join(file_list))
    _run([cscope, "-q", "-b", "-k", "-i", str(cscope_files),
          "-f", str(root / "_callgraph_cscope.out")], timeout=DEFAULT_TIMEOUT * 2)

    # 3. For each known function, query "who does this call?"
    callgraph: dict[str, set[str]] = {}
    for name in list(func_defs.keys())[:max_nodes]:
        result = _run([cscope, "-d", "-L", "-2", name,
                       "-f", str(root / "_callgraph_cscope.out")], timeout=30)
        callees: set[str] = set()
        for ln in (result.get("stdout") or "").splitlines():
            # cscope -L -2 output: <file> <function> <line> <text>
            toks = ln.split()
            if len(toks) >= 4:
                callees.add(toks[1])
        callgraph[name] = callees

    # 4. Cross-reference with coverage to find reached functions.
    reached: set[str] = set()
    if cov_lcov_path and Path(cov_lcov_path).is_file():
        for raw in Path(cov_lcov_path).read_text(errors="replace").splitlines():
            if raw.startswith("FNDA:"):
                try:
                    hits_str, name = raw[5:].split(",", 1)
                    if int(hits_str) > 0:
                        reached.add(name)
                except ValueError:
                    pass

    # 5. Emit dot.
    dot_path = Path(output_dot) if output_dot else (root / "_callgraph.dot")
    lines = ["digraph callgraph {", '  rankdir=LR;', '  node [shape=box, fontsize=10, style=filled];']
    nodes_emitted: set[str] = set()
    for fn, callees in callgraph.items():
        nodes_emitted.add(fn)
        nodes_emitted.update(callees)
        for c in callees:
            lines.append(f'  "{fn}" -> "{c}";')
    for fn in nodes_emitted:
        if fn in reached:
            color = "#bdf7bd"  # reached
        elif fn in func_defs:
            color = "#f7bdbd"  # known but unreached
        else:
            color = "#dddddd"  # external
        lines.append(f'  "{fn}" [fillcolor="{color}"];')
    lines.append("}")
    dot_path.write_text("\n".join(lines))

    # 6. Render to SVG if dot is available.
    svg_path = Path(output_svg) if output_svg else dot_path.with_suffix(".svg")
    rendered = ""
    dot_bin = _which("dot")
    if dot_bin:
        r = _run([dot_bin, "-Tsvg", str(dot_path), "-o", str(svg_path)], timeout=DEFAULT_TIMEOUT)
        if r["exit_code"] == 0 and svg_path.is_file():
            rendered = str(svg_path)

    return {
        "ok": True,
        "dot_path": str(dot_path),
        "svg_path": rendered,
        "functions_total": len(func_defs),
        "functions_in_graph": len(callgraph),
        "functions_reached": len(reached & nodes_emitted),
        "functions_unreached": len(nodes_emitted) - len(reached & nodes_emitted),
        # Convenience: the unreached set, sorted, capped to a sensible size
        # for downstream consumers (vuln reports, dashboards).
        "unreached_function_names": sorted(
            (nodes_emitted & set(func_defs.keys())) - reached
        )[:max_nodes],
    }


