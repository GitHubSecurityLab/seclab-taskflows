@mcp.tool()
def run_coverage(
    cov_binary_path: Annotated[str, Field(description="Path to the coverage-instrumented binary built by compile_harness")],
    inputs_dir: Annotated[str, Field(description="Directory of inputs to replay (typically the AFL queue directory)")],
    output_dir: Annotated[str, Field(description="Where to write profraw/profdata/lcov files")],
    stdin_input: Annotated[bool, Field(description="If true, replay each input by piping it on stdin")] = True,
    extra_args: Annotated[list[str] | None, Field(description="Extra CLI args for the binary, e.g. ['@@'] when stdin_input is false")] = None,
    per_input_timeout: Annotated[int, Field(description="Timeout (s) per individual input replay")] = 5,
    max_inputs: Annotated[int, Field(description="Hard cap on inputs to replay (oldest first)")] = 5000,
) -> dict:
    """Replay ``inputs_dir`` against the coverage binary, then export an LCOV file.

    Prefers llvm-cov / llvm-profdata; falls back to gcov+lcov when those tools
    are missing or the binary wasn't built with profile-instr-generate.
    """
    extra_args = extra_args or []
    cov_bin = Path(cov_binary_path)
    if not cov_bin.is_file():
        return {"ok": False, "error": f"binary not found: {cov_binary_path}"}
    in_dir = Path(inputs_dir)
    if not in_dir.is_dir():
        return {"ok": False, "error": f"inputs dir not found: {inputs_dir}"}
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    profraw_dir = out / "profraw"
    profraw_dir.mkdir(exist_ok=True)
    lcov_path = out / "coverage.lcov"

    # Collect inputs (queue files only, skip AFL bookkeeping subdirs).
    inputs = []
    for entry in sorted(in_dir.rglob("*")):
        if entry.is_file() and not entry.name.startswith(".") and entry.name != "fuzzer_stats":
            inputs.append(entry)
            if len(inputs) >= max_inputs:
                break

    replays_ok = 0
    replays_fail = 0
    for i, blob in enumerate(inputs):
        env = {"LLVM_PROFILE_FILE": str(profraw_dir / f"r{i}.profraw")}
        if stdin_input:
            try:
                proc = subprocess.run(
                    [str(cov_bin)], input=blob.read_bytes(), capture_output=True,
                    timeout=per_input_timeout, env={**os.environ, **env},
                )
                replays_ok += 1 if proc.returncode == 0 else 0
                replays_fail += 0 if proc.returncode == 0 else 1
            except subprocess.TimeoutExpired:
                replays_fail += 1
        else:
            args = [str(cov_bin)] + [str(blob) if a == "@@" else a for a in extra_args]
            try:
                proc = subprocess.run(args, capture_output=True, timeout=per_input_timeout,
                                      env={**os.environ, **env})
                replays_ok += 1 if proc.returncode == 0 else 0
                replays_fail += 0 if proc.returncode == 0 else 1
            except subprocess.TimeoutExpired:
                replays_fail += 1

    profdata_path = out / "merged.profdata"
    llvm_profdata = _which("llvm-profdata")
    llvm_cov = _which("llvm-cov")

    if llvm_profdata and llvm_cov:
        merge = _run([llvm_profdata, "merge", "-sparse", "-o", str(profdata_path), str(profraw_dir)])
        if merge["exit_code"] != 0:
            return {"ok": False, "error": "llvm-profdata merge failed", **merge}
        # llvm-cov LCOV output can be many MB — stream straight to the file
        # rather than going through _run() (which truncates stdout).
        try:
            with lcov_path.open("w") as fh:
                proc = subprocess.run(
                    [llvm_cov, "export", "-format=lcov", str(cov_bin),
                     "-instr-profile", str(profdata_path)],
                    stdout=fh, stderr=subprocess.PIPE, text=True, timeout=DEFAULT_TIMEOUT,
                )
        except subprocess.TimeoutExpired:
            return {"ok": False, "error": "llvm-cov export timed out"}
        if proc.returncode != 0:
            return {"ok": False, "error": "llvm-cov export failed", "stderr": proc.stderr[-2000:]}

        # E1: HTML annotated source coverage. Best-effort; non-fatal if it fails.
        html_dir = out / "html"
        html_dir.mkdir(parents=True, exist_ok=True)
        html_path = ""
        try:
            html_proc = subprocess.run(
                [llvm_cov, "show", "-format=html", "-output-dir", str(html_dir),
                 str(cov_bin), "-instr-profile", str(profdata_path)],
                capture_output=True, text=True, timeout=DEFAULT_TIMEOUT,
            )
            index = html_dir / "index.html"
            if html_proc.returncode == 0 and index.is_file():
                html_path = str(index)
        except subprocess.TimeoutExpired:
            html_path = ""
        return {
            "ok": True, "lcov_path": str(lcov_path), "html_path": html_path,
            "inputs_replayed": len(inputs), "replays_ok": replays_ok, "replays_fail": replays_fail,
            "tool": "llvm-cov",
        }

    # Fallback: gcov + lcov
    gcov = _which("gcov")
    lcov_bin = _which("lcov")
    if not (gcov and lcov_bin):
        return {"ok": False, "error": "neither llvm-cov nor lcov is available; run install_afl.sh"}
    base_dir = str(cov_bin.parent)
    capture = _run([lcov_bin, "--capture", "--directory", base_dir, "--output-file", str(lcov_path),
                    "--ignore-errors", "source,gcov,unused"], timeout=DEFAULT_TIMEOUT)
    if capture["exit_code"] != 0:
        return {"ok": False, "error": "lcov capture failed", **capture}
    # E1: genhtml renders LCOV as HTML; non-fatal if missing.
    html_path = ""
    genhtml = _which("genhtml")
    if genhtml:
        html_dir = out / "html"
        html_dir.mkdir(parents=True, exist_ok=True)
        gh = _run([genhtml, "-o", str(html_dir), str(lcov_path),
                   "--ignore-errors", "source"], timeout=DEFAULT_TIMEOUT)
        if gh["exit_code"] == 0 and (html_dir / "index.html").is_file():
            html_path = str(html_dir / "index.html")
    return {
        "ok": True, "lcov_path": str(lcov_path), "html_path": html_path,
        "inputs_replayed": len(inputs), "replays_ok": replays_ok, "replays_fail": replays_fail,
        "tool": "gcov+lcov",
    }


