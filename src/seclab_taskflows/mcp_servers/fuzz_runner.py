@mcp.tool()
def cmin(
    afl_binary_path: Annotated[str, Field(description="Path to the AFL-instrumented binary")],
    input_dir: Annotated[str, Field(description="Directory of inputs to minimise (e.g. <run>/default/queue)")],
    output_dir: Annotated[str, Field(description="Directory where the minimised corpus will be written")],
    stdin_input: Annotated[bool, Field(description="If true, drive via stdin (default for libAFLDriver-style binaries is false)")] = False,
    extra_args: Annotated[list[str] | None, Field(description="Extra CLI args; '@@' replaced with input path")] = None,
    timeout_seconds: Annotated[int, Field(description="Total afl-cmin timeout")] = 300,
) -> dict:
    """Minimise a corpus with ``afl-cmin`` (drops inputs that don't add coverage)."""
    extra_args = list(extra_args or [])
    tool = _which("afl-cmin")
    if not tool:
        return {"ok": False, "error": "afl-cmin not on PATH"}
    if not Path(afl_binary_path).is_file():
        return {"ok": False, "error": f"binary not found: {afl_binary_path}"}
    if not Path(input_dir).is_dir():
        return {"ok": False, "error": f"input dir not found: {input_dir}"}
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    target_args = extra_args if (stdin_input or "@@" in extra_args) else [*extra_args, "@@"]
    cmd = [tool, "-i", input_dir, "-o", output_dir, "--", afl_binary_path, *target_args]
    result = _run(cmd, timeout=timeout_seconds, env=AFL_ENV_DEFAULTS)
    in_count = sum(1 for _ in Path(input_dir).rglob("*") if _.is_file())
    out_count = sum(1 for _ in Path(output_dir).rglob("*") if _.is_file())
    return {
        "ok": result["exit_code"] == 0 and out_count > 0,
        "input_count": in_count,
        "minimised_count": out_count,
        "exit_code": result["exit_code"],
        "stderr_tail": result["stderr"],
    }


_PRINTABLE_RE = re.compile(rb"[ -~]{4,32}")


@mcp.tool()
def extract_dictionary(
    binary_path: Annotated[str, Field(description="Path to a binary or object file to mine for printable strings")],
    output_path: Annotated[str, Field(description="Where to write the AFL dictionary file")],
    min_length: Annotated[int, Field(description="Minimum string length to include")] = 4,
    max_length: Annotated[int, Field(description="Maximum string length to include")] = 32,
    max_entries: Annotated[int, Field(description="Cap on dictionary size to keep AFL fast")] = 256,
) -> dict:
    """Extract printable substrings from a binary as an AFL dictionary.

    Heuristic: scan for ASCII runs between ``min_length`` and ``max_length``,
    drop common library noise (compiler banners, ELF section names, file
    paths), keep the rest. Result is written in AFL ``-x`` dictionary format.
    """
    src = Path(binary_path)
    if not src.is_file():
        return {"ok": False, "error": f"binary not found: {binary_path}"}
    data = src.read_bytes()
    seen: set[bytes] = set()
    skip_substrings = (
        b"GCC:", b"clang version", b"GLIBC_", b".text", b".rodata", b".data",
        b".bss", b".init", b".fini", b"/usr/", b"/home/", b"/tmp/", b"/lib",
        b"AddressSanitizer", b"UndefinedBehavior", b"libstdc++", b"libc.so",
    )
    for m in _PRINTABLE_RE.finditer(data):
        s = m.group(0)
        if len(s) < min_length or len(s) > max_length:
            continue
        if any(skip in s for skip in skip_substrings):
            continue
        # Drop strings that look like file paths or symbol mangling
        if s.startswith(b"_Z") or s.startswith(b"/"):
            continue
        seen.add(s)
        if len(seen) >= max_entries:
            break
    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w") as fh:
        fh.write("# AFL dictionary auto-extracted by fuzz_runner.extract_dictionary\n")
        for i, s in enumerate(sorted(seen)):
            escaped = "".join(
                f"\\x{b:02x}" if (b < 0x20 or b >= 0x7f or b in b'"\\') else chr(b) for b in s
            )
            fh.write(f'kw{i}="{escaped}"\n')
    return {"ok": True, "dictionary_path": str(out), "entries": len(seen)}


