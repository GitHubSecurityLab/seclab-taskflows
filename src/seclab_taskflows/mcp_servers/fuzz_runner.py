_AFL_STATS_RE = re.compile(r"^([a-z_0-9]+)\s*:\s*(.+)$", re.MULTILINE)


def _read_afl_stats(out_dir: Path) -> dict:
    stats_file = out_dir / "default" / "fuzzer_stats"
    if not stats_file.is_file():
        # Some AFL versions write directly under out_dir
        stats_file = out_dir / "fuzzer_stats"
    if not stats_file.is_file():
        return {}
    text = stats_file.read_text(errors="replace")
    out: dict = {}
    for m in _AFL_STATS_RE.finditer(text):
        out[m.group(1)] = m.group(2).strip()
    return out


@mcp.tool()
def run_afl_for(
    afl_binary_path: Annotated[str, Field(description="Path to the AFL-instrumented binary built by compile_harness")],
    seed_dir: Annotated[str, Field(description="Directory containing seed inputs (will be created if missing; AFL needs at least one seed)")],
    output_dir: Annotated[str, Field(description="afl-fuzz output directory (-o); will be created if missing")],
    seconds: Annotated[int, Field(description="Wall-clock budget for this campaign")] = 30,
    dictionary: Annotated[str, Field(description="Optional path to an AFL dictionary (-x)")] = "",
    stdin_input: Annotated[bool, Field(description="If true, AFL pipes input on stdin (no @@ argument)")] = True,
    extra_args: Annotated[list[str] | None, Field(description="Extra CLI arguments appended after '--' for the target binary, e.g. ['@@']")] = None,
    memory_limit_mb: Annotated[int, Field(description="Memory limit per execution in MB (-m); 0 = unlimited. Used to detect OOM.")] = 1024,
    timeout_ms: Annotated[int, Field(description="Per-execution timeout in milliseconds (-t); 0 = AFL default. Used to detect algorithmic blow-up.")] = 0,
    parallel_workers: Annotated[int, Field(description="Number of AFL workers to run in parallel (1 master + N-1 secondaries sharing the -o dir). 1 = single instance.")] = 1,
) -> dict:
    """Run afl-fuzz for ``seconds`` and parse fuzzer_stats afterwards.

    With ``parallel_workers > 1`` we spawn one master (``-M default``) and
    N-1 secondaries (``-S secondary_<i>``) that share the same ``-o`` output
    directory; AFL then synchronises their queues automatically. Stats are
    summed across workers.
    """
    extra_args = extra_args or []
    afl_fuzz = _which("afl-fuzz")
    if not afl_fuzz:
        return {"ok": False, "error": "afl-fuzz not on PATH (run install_afl.sh)"}
    if not Path(afl_binary_path).is_file():
        return {"ok": False, "error": f"binary not found: {afl_binary_path}"}

    seed_path = Path(seed_dir)
    seed_path.mkdir(parents=True, exist_ok=True)
    if not any(seed_path.iterdir()):
        # AFL refuses to start without at least one seed.
        (seed_path / "seed_0").write_bytes(b"0")

    out_path = Path(output_dir)
    out_path.mkdir(parents=True, exist_ok=True)

    if not stdin_input and not extra_args:
        extra_args = ["@@"]

    def _build_cmd(worker_arg: list[str]) -> list[str]:
        cmd = [afl_fuzz, "-i", str(seed_path), "-o", str(out_path), "-V", str(seconds)]
        if memory_limit_mb > 0:
            cmd += ["-m", str(memory_limit_mb)]
        if timeout_ms > 0:
            cmd += ["-t", str(timeout_ms)]
        if dictionary and Path(dictionary).is_file():
            cmd += ["-x", dictionary]
        cmd += worker_arg
        cmd += ["--", afl_binary_path, *extra_args]
        return cmd

    if parallel_workers <= 1:
        cmd = _build_cmd([])
        result = _run(cmd, timeout=seconds + 60, env=AFL_ENV_DEFAULTS)
        results = [result]
        worker_dirs = ["default"]
    else:
        worker_dirs = ["default", *(f"secondary_{i}" for i in range(1, parallel_workers))]
        cmds = [_build_cmd(["-M", "default"])]
        cmds += [_build_cmd(["-S", w]) for w in worker_dirs[1:]]
        # Spawn all in parallel, then wait.
        procs = []
        for c in cmds:
            real_env = {**os.environ, **AFL_ENV_DEFAULTS}
            procs.append(subprocess.Popen(c, env=real_env, stdout=subprocess.DEVNULL, stderr=subprocess.PIPE))
        results = []
        for p in procs:
            try:
                _, err = p.communicate(timeout=seconds + 90)
                results.append({"exit_code": p.returncode, "stderr": (err or b"")[-2000:].decode(errors="replace"), "stdout": ""})
            except subprocess.TimeoutExpired:
                p.kill()
                results.append({"exit_code": None, "stderr": "timeout", "stdout": ""})

    # Aggregate stats across worker directories.
    agg = {"execs_per_sec": 0.0, "corpus_count": 0, "saved_crashes": 0, "saved_hangs": 0}
    per_worker_stats: list[dict] = []
    for w in worker_dirs:
        wd = out_path / w
        if not wd.is_dir():
            # Single-instance default lives directly under out_path / "default" or out_path
            wd = out_path
        stats_file = wd / "fuzzer_stats"
        if not stats_file.is_file():
            stats_file = out_path / "default" / "fuzzer_stats"
        if not stats_file.is_file():
            continue
        text = stats_file.read_text(errors="replace")
        wstats = {m.group(1): m.group(2).strip() for m in _AFL_STATS_RE.finditer(text)}
        per_worker_stats.append(wstats)
        try:
            agg["execs_per_sec"] += float(wstats.get("execs_per_sec", "0") or 0)
            agg["corpus_count"] = max(agg["corpus_count"], int(wstats.get("corpus_count", "0") or 0))
            agg["saved_crashes"] = max(agg["saved_crashes"], int(wstats.get("saved_crashes", "0") or 0))
            agg["saved_hangs"] = max(agg["saved_hangs"], int(wstats.get("saved_hangs", "0") or 0))
        except ValueError:
            pass

    # The crashes/queue dirs we report are always the master.
    queue_dir = str(out_path / "default" / "queue")
    crashes_dir = str(out_path / "default" / "crashes")
    hangs_dir = str(out_path / "default" / "hangs")

    return {
        "ok": all(r["exit_code"] == 0 or r["exit_code"] is None for r in results),
        "exit_code": results[0]["exit_code"],
        "stdout_tail": results[0].get("stdout", ""),
        "stderr_tail": results[0].get("stderr", ""),
        "stats": per_worker_stats[0] if per_worker_stats else {},
        "per_worker_stats": per_worker_stats,
        "exec_per_sec": agg["execs_per_sec"],
        "paths_total": agg["corpus_count"],
        "crashes_count": agg["saved_crashes"],
        "hangs_count": agg["saved_hangs"],
        "queue_dir": queue_dir,
        "crashes_dir": crashes_dir,
        "hangs_dir": hangs_dir,
        "output_dir": str(out_path),
        "parallel_workers": parallel_workers,
    }


