def _which(name: str) -> str | None:
    return shutil.which(name)


def _afl_install_root() -> Path | None:
    """Locate the AFL++ install prefix by walking up from afl-fuzz."""
    afl = _which("afl-fuzz")
    if not afl:
        return None
    p = Path(afl).resolve().parent.parent
    return p if (p / "lib" / "afl").is_dir() else None


def _afl_driver_archive() -> Path | None:
    root = _afl_install_root()
    if not root:
        return None
    candidate = root / "lib" / "afl" / "libAFLDriver.a"
    return candidate if candidate.is_file() else None


_STANDALONE_DRIVER_C = r"""
/* Auto-generated by seclab_taskflows fuzz_runner.
 * Standalone main() for libFuzzer-style harnesses, used by the coverage build
 * (which is plain clang and cannot link AFL's libAFLDriver.a).
 *
 * Usage:
 *   demo.cov < input          # read from stdin
 *   demo.cov file1 file2 ...  # iterate over files
 */
#include <stddef.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <sys/stat.h>
#include <unistd.h>

extern int LLVMFuzzerTestOneInput(const uint8_t *data, size_t size);
__attribute__((weak)) int LLVMFuzzerInitialize(int *argc, char ***argv);

static int run_file(const char *path) {
    FILE *f = fopen(path, "rb");
    if (!f) { perror(path); return 1; }
    if (fseek(f, 0, SEEK_END) != 0) { fclose(f); return 1; }
    long n = ftell(f);
    if (n < 0) { fclose(f); return 1; }
    rewind(f);
    uint8_t *buf = (uint8_t *)malloc((size_t)n);
    if (!buf) { fclose(f); return 1; }
    size_t got = fread(buf, 1, (size_t)n, f);
    fclose(f);
    LLVMFuzzerTestOneInput(buf, got);
    free(buf);
    return 0;
}

int main(int argc, char **argv) {
    if (LLVMFuzzerInitialize) LLVMFuzzerInitialize(&argc, &argv);
    if (argc <= 1) {
        size_t cap = 4096, len = 0;
        uint8_t *buf = (uint8_t *)malloc(cap);
        if (!buf) return 1;
        for (;;) {
            if (len == cap) {
                cap *= 2;
                uint8_t *nb = (uint8_t *)realloc(buf, cap);
                if (!nb) { free(buf); return 1; }
                buf = nb;
            }
            ssize_t r = read(0, buf + len, cap - len);
            if (r < 0) { free(buf); return 1; }
            if (r == 0) break;
            len += (size_t)r;
        }
        LLVMFuzzerTestOneInput(buf, len);
        free(buf);
        return 0;
    }
    for (int i = 1; i < argc; i++) {
        struct stat st;
        if (stat(argv[i], &st) == 0 && S_ISREG(st.st_mode)) run_file(argv[i]);
    }
    return 0;
}
"""


def _materialise_standalone_driver() -> Path:
    """Write the standalone driver to the workspace and return its path."""
    drv = WORKSPACE / "_aflpp_standalone_driver.c"
    if not drv.is_file() or drv.read_text() != _STANDALONE_DRIVER_C:
        drv.write_text(_STANDALONE_DRIVER_C)
    return drv


def _run(cmd: list[str], cwd: str | None = None, timeout: int = DEFAULT_TIMEOUT,
         env: dict | None = None) -> dict:
    """Run a subprocess; never raise. Return a dict suitable for the agent."""
    real_env = os.environ.copy()
    if env:
        real_env.update(env)
    logging.debug("exec: %s (cwd=%s timeout=%d)", " ".join(shlex.quote(c) for c in cmd), cwd, timeout)
    try:
        proc = subprocess.run(
            cmd, cwd=cwd, env=real_env, capture_output=True, text=True, timeout=timeout,
        )
    except subprocess.TimeoutExpired as e:
        return {
            "exit_code": None, "timed_out": True,
            "stdout": (e.stdout or "")[-4000:] if isinstance(e.stdout, str) else "",
            "stderr": (e.stderr or "")[-4000:] if isinstance(e.stderr, str) else "",
            "cmd": " ".join(shlex.quote(c) for c in cmd),
        }
    except FileNotFoundError as e:
        return {"exit_code": 127, "timed_out": False, "stdout": "", "stderr": str(e),
                "cmd": " ".join(shlex.quote(c) for c in cmd)}
    return {
        "exit_code": proc.returncode, "timed_out": False,
        "stdout": proc.stdout[-4000:], "stderr": proc.stderr[-4000:],
        "cmd": " ".join(shlex.quote(c) for c in cmd),
    }


