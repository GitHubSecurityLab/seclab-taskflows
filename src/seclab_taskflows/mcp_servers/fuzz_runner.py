# E3: untouched API surface (Fuzz-Introspector-style)
# ---------------------------------------------------------------------------

# Heuristic for "public-API function" names. Anything starting with one of
# these prefixes (after stripping leading underscores) is treated as public.
# This is intentionally broad so we don't miss anything; the agent can
# narrow further via the `extern_only_headers` argument.
_PUBLIC_PREFIX_HINTS = (
    # Common library naming conventions
    "lzma_", "json_", "cJSON_", "XML_", "expat_", "onig_", "regex_",
    "ssl_", "tls_", "crypto_", "ec_", "rsa_", "evp_", "x509_",
    "png_", "jpeg_", "tiff_", "gif_",
    "av_", "avformat_", "avcodec_",
    "sqlite3_", "pcre_", "pcre2_",
    # Generic catch-all for "looks public"
)


@mcp.tool()
def untouched_api_surface(
    source_root: Annotated[str, Field(description="Path to the unpacked source tree (e.g. the repo root)")],
    reached_function_names: Annotated[list[str], Field(description="Function names already reached by any fuzzing run (use get_repo_reached_functions on fuzz_context for the full set)")],
    extern_only_headers: Annotated[list[str] | None, Field(description="Optional list of glob patterns for public-API headers (e.g. ['include/**/*.h']); when set, only function declarations from these headers are considered public")] = None,
    max_results: Annotated[int, Field(description="Cap on the number of unreached function names to return")] = 200,
) -> dict:
    """Compute the set of public-API functions that no fuzzing run has reached.

    Two strategies, run together and unioned:
      1. ctags --c-kinds=p (function prototypes) restricted to ``extern_only_headers``
         globs. This is the strict / "documented public API" set.
      2. ctags --c-kinds=f (function definitions) filtered by name prefix
         (heuristic: starts with one of a small set of common library prefixes).
         This catches public functions that don't have a separate prototype in
         a public header but follow naming conventions.

    Returns the never-reached subset, sorted by name, capped at ``max_results``.
    """
    root = Path(source_root)
    if not root.is_dir():
        return {"ok": False, "error": f"source_root not found: {source_root}"}

    ctags = _which("ctags")
    if not ctags:
        return {"ok": False, "error": "ctags is required (install_afl.sh installs universal-ctags)"}

    public_funcs: set[str] = set()

    # Strategy 1: prototypes from public headers.
    if extern_only_headers:
        header_files: list[str] = []
        for pat in extern_only_headers:
            header_files.extend(_glob.glob(str(root / pat), recursive=True))
        if header_files:
            tags_file = root / "_untouched_tags_p"
            _run([ctags, "--c-kinds=p", "--fields=+ne", "--languages=C,C++",
                  "-f", str(tags_file), *header_files], timeout=DEFAULT_TIMEOUT)
            if tags_file.is_file():
                for line in tags_file.read_text(errors="replace").splitlines():
                    if line.startswith("!"):
                        continue
                    name = line.split("\t", 1)[0]
                    if name and not name.startswith("_"):
                        public_funcs.add(name)

    # Strategy 2: definitions whose name starts with a known prefix.
    tags_file = root / "_untouched_tags_f"
    _run([ctags, "-R", "--c-kinds=f", "--fields=+ne", "--languages=C,C++",
          "-f", str(tags_file), str(root)], timeout=DEFAULT_TIMEOUT)
    all_defs: set[str] = set()
    if tags_file.is_file():
        for line in tags_file.read_text(errors="replace").splitlines():
            if line.startswith("!"):
                continue
            name = line.split("\t", 1)[0]
            if name and not name.startswith("_"):
                all_defs.add(name)
                if any(name.startswith(p) for p in _PUBLIC_PREFIX_HINTS):
                    public_funcs.add(name)

    reached = set(reached_function_names or [])
    untouched = sorted(public_funcs - reached)
    return {
        "ok": True,
        "public_total": len(public_funcs),
        "reached_total": len(reached & public_funcs),
        "untouched_total": len(untouched),
        "untouched": untouched[:max_results],
        "definitions_total": len(all_defs),
    }


